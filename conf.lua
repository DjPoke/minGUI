function love.conf(t)
    t.version = "11.4"
    t.console = true

    t.window.title = "minGUI"
    t.window.icon = nil
    t.window.width = 640
    t.window.height = 480
    t.window.borderless = false
    t.window.resizable = false
    t.window.fullscreen = false
    t.window.vsync = 1
    t.window.msaa = 0
    t.window.display = 1
    t.window.x = nil
    t.window.y = nil
end